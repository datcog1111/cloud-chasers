import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getClouds = () => new Promise((resolve, reject) => {
  fetch(`${endpoint}/cloud.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(Object.values(data)))
    .catch(reject);
});

const createCloud = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/cloud.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const getSingleCloud = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/cloud/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const deleteCloud = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/cloud/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

const updateCloud = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/cloud/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});

export {
  getClouds, createCloud, getSingleCloud, deleteCloud, updateCloud,
};
